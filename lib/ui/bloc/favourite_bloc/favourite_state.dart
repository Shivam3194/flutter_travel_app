  List<Object?> get props => [];
}

class FavouriteLoadedState extends FavouriteState {
  @override
  List<Object?> get props => [];
}

class FavouriteErrorState extends FavouriteState {
  @override
  List<Object?> get props => [];
}

class FavouriteEmptyState extends FavouriteState {
  @override
  List<Object?> get props => [];
}
